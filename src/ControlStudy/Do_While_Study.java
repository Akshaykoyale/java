package ControlStudy;

public class Do_While_Study {

	public static void main(String[] args) {
		int a=2;
		do {
				System.out.println(a);
			a=a+2;
			
		} while (a<=20);
	System.out.println("====================");
	int b=20;
	do {
		System.out.println(b);
		b=b-2;
	} while (b>=2);
	
	
		
	
	}
}

